package assignment2_refactored.Enums;

/** Categorizes Mount type for a Cabinet
 * FLOOR means the cabinet only mounts to the floor
 * WALL means the cabinet only mounts to the wall
 * FLOOR_OR_WALL means the cabinet can either mount to the floor or wall
 */
public enum MountType {
  FLOOR,
  WALL,
  FLOOR_OR_WALL,
}

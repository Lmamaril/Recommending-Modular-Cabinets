/** Categorizes a Rail type for a cabinet *
 * NOT_REQUIRED means Rails are not required for the cabinet.
 * STANDARD means that Standard Rails go with the cabinet.
 * STYLE_SPECIFIC means that a Style Specific Rail is made must be paired with a cabinet
 */

public enum Rails {
  NOT_REQUIRED,
  STANDARD,
  STYLE_SPECIFIC
}
